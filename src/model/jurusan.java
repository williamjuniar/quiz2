package model;

/**
 *
 * @author ASUS
 */
public class jurusan {
    private String kode,nama;

    public jurusan(){
    
    }
    public jurusan(String kode, String nama) {
        this.kode = kode;
        this.nama = nama;
    }

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
    
    
}
